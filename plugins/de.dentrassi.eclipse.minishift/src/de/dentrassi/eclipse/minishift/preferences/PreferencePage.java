package de.dentrassi.eclipse.minishift.preferences;

import org.eclipse.jface.preference.FieldEditorPreferencePage;
import org.eclipse.jface.preference.FileFieldEditor;
import org.eclipse.ui.IWorkbench;
import org.eclipse.ui.IWorkbenchPreferencePage;

import de.dentrassi.eclipse.minishift.Activator;

public class PreferencePage extends FieldEditorPreferencePage implements IWorkbenchPreferencePage {

	public PreferencePage() {
		super(GRID);
		setPreferenceStore(Activator.getDefault().getPreferenceStore());
		setDescription("Minishift configuration");
	}

	@Override
	public void createFieldEditors() {
		addField(
				new FileFieldEditor(PreferenceConstants.P_MINISHIFT_PATH, "&Executable:", true, getFieldEditorParent()));

	}

	@Override
	public void init(final IWorkbench workbench) {
	}

}